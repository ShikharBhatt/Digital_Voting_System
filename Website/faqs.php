      </tr>
      <tr>
        <td>Ans</td>
        <td><textarea disabled="true" rows="4" cols="100">The voting process has basically 3 stages :

Stage 1: Visit your nearest polling station. Before you can vote you will be provided with a password by the Officer stationed at that polling station.

Stage 2: You will be provided with a machine where you have to enter your credentials(i.e. VOTER ID & PASSWORD).
        
Stage 3: On successful login you will see the names of the information of the candidates of your constituency and a radio button for the respective candidates. You will have 45 seconds(for normal voters) or 60 seconds(for physically challenged and senior citizens) to vote for your candidate.</textarea></td>
